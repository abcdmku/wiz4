  /* =========================== FLAME JET =====================
     16x64. Frames 0,1 = burning; 2,3 = OFF (blank) — the jet
     cycles by letting the animation index slide onto blank frames.  */
  function flameArt(seed) {
    const rows = [];
    for (let y = 0; y < 64; y++) {
      let row = '';
      const taper = y < 8 ? 2 + (y >> 2) : y < 48 ? 3 + ((y + seed * 3) % 3) : 5;
      const c0 = 8 - taper, c1 = 8 + taper;
      for (let x = 0; x < 16; x++) {
        if (x < c0 || x > c1) { row += '.'; continue; }
        const edge = (x === c0 || x === c1);
        const r = (x * 7 + y * 13 + seed * 29) % 10;
        if (y < 6) row += edge ? 'e' : (r < 4 ? 'e' : 't');
        else if (edge) row += r < 3 ? '.' : 't';
        else if (r < 2) row += 'e';
        else if (r < 7) row += 't';
        else row += 'T';
      }
      rows.push(row);
    }
    return f(rows.join('\n'));
  }
