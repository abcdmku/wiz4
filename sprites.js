  function glitterArt(big) {
    const rows = [];
    for (let y = 0; y < 32; y++) {
      let row = '';
      for (let x = 0; x < 32; x++) {
        const dx = x - 16, dy = y - 16;
        const d = Math.sqrt(dx * dx + dy * dy);
        const ring = big ? 13 : 9;
        const on = Math.abs(d - ring) < 1.2 && ((x * 5 + y * 3) % 4 === 0);
        const in2 = Math.abs(d - ring / 2) < 1 && ((x * 3 + y * 7) % 5 === 0);
        row += on ? (((x + y) & 2) ? 'a' : 'G') : in2 ? 'c' : '.';
      }
      rows.push(row);
    }
    return f(rows.join('\n'));
  }
