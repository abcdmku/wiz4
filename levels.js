/* ------------------------------- compiler ------------------------------- */

const SPRITE_ID = {
  g: 1, n: 2, d: 3, v: 6, V: 6, h: 7, H: 7, x: 9, s: 11, b: 13, j: 14,
  O: 15, _: 17, f: 18, a: 19, Z: 20, G: 21, X: 22,
};
const SPRITE_FLAG = { v: 96, V: -160, h: 128, H: -224 };

function compileLevel(def) {
  const MAPW = 256, MAPH = 16;
  const rows = [];
  for (let y = 0; y < MAPH; y++) {
    let r = def.sections.map((s, si) => {
      let line = s[y] || '';
      if (line.length !== 32) {
        console.warn(`[${def.name}] section ${si} row ${y} has length ${line.length}`);
        line = (line + '.'.repeat(32)).slice(0, 32);
      }
      return line;
    }).join('');
    rows.push((r + '.'.repeat(MAPW)).slice(0, MAPW));
  }

  const th = TILES.THEMES[def.theme];
  const cells = [];
  for (let x = 0; x < MAPW; x++) {
    cells.push([]);
    for (let y = 0; y < MAPH; y++) {
      cells[x].push({
        block: 0, bonus: 0, sprite: 0, sflag: 0, spawned: false,
        cp: 0, locked: false, boundary: false,
        fore: null, back: null, toggle: false, item: 0,
      });
    }
  }
  const doors = [];   // {id,x,y,locked,open}
  const decos = [];   // {frame|frames,x,y,kind}
  const ways = [];    // checkpoint waystones {x,y}
  let start = null, valid = true;
  const at = (x, y) => (x >= 0 && x < MAPW && y >= 0 && y < MAPH) ? rows[y][x] : '.';
  const solidChar = (c) => c === '#' || c === 'B' || c === '$';

  for (let y = 0; y < MAPH; y++) {
    for (let x = 0; x < MAPW; x++) {
      const ch = rows[y][x];
      const cell = cells[x][y];
      switch (ch) {
        case '.': break;
        case '#': case 'B': {
          cell.block = 1;
          const set = ch === '#' ? th.ground : th.solid;
          const exposed = !solidChar(at(x, y - 1));
          let t = exposed ? set.cap : set.fill;
          if (!exposed && set.alt && ((x * 7 + y * 13) % 9 === 0)) t = set.alt;
          cell.fore = t;
          break;
        }
        case '=': cell.block = 2; cell.fore = TILES.wood; break;
        case '$': cell.block = 1; cell.fore = TILES.runeOn; cell.toggle = true; break;
        case '%': cell.block = 4; cell.fore = TILES.runeOff; cell.toggle = true; break;
        case 'S': {
          cell.fore = null; // spring visual is a deco; bonus lives on the block below
          if (y + 1 < MAPH) cells[x][y + 1].bonus = 10;
          decos.push({ kind: 'spring', x, y });
          break;
        }
        case '/': cell.bonus = 8; decos.push({ kind: 'lever', x, y }); break;
        case '*': cell.bonus = 1; break;
        case 'o': cell.bonus = 2; break;
        case 'L': cell.bonus = 3; break;
        case 'K': cell.bonus = 4; break;
        case 'I': cell.bonus = 6; break;
        case 'c': {
          cell.bonus = 5;
          ways.push({ x, y });
          break;
        }
        case '!': {
          for (let dx = 0; dx < 2; dx++)
            for (let dy = -1; dy <= 0; dy++) {
              const c2 = cells[x + dx] && cells[x + dx][y + dy];
              if (c2) c2.bonus = 9;
            }
          decos.push({ kind: 'exit', x, y });
          break;
        }
        case 'P': cell.cp = 1; start = { x, y }; break;
        case '|': cell.boundary = true; break;
        case 'T': decos.push({ kind: 'torch', x, y }); break;
        case 'W': cell.back = TILES.windowT; break;
        case 'N': cell.back = TILES.banner; break;
        default: {
          if (ch >= '1' && ch <= '8') {
            const id = 8 + (ch.charCodeAt(0) - 49); // ids 8..15
            const locked = (def.locked || []).includes(ch.charCodeAt(0) - 48);
            for (let dx = 0; dx < 2; dx++)
              for (let dy = -1; dy <= 0; dy++) {
                const c2 = cells[x + dx] && cells[x + dx][y + dy];
                if (c2) { c2.bonus = 7; c2.cp = id; c2.locked = locked; }
              }
            doors.push({ id, x, y, locked, open: false });
          } else if (SPRITE_ID[ch] != null) {
            cell.sprite = SPRITE_ID[ch];
            cell.sflag = SPRITE_FLAG[ch] || 0;
          } else {
            console.warn(`[${def.name}] unknown char '${ch}' at ${x},${y}`);
          }
        }
      }
    }
  }

  // auto flora on exposed ground
  let rng = 1234 + def.name.length * 77;
  const rnd = () => (rng = (rng * 1103515245 + 12345) & 0x7fffffff) / 0x7fffffff;
  for (let x = 1; x < MAPW - 1; x++) {
    for (let y = 1; y < MAPH; y++) {
      const c = cells[x][y], above = cells[x][y - 1];
      if (c.block === 1 && c.fore && !above.block && !above.bonus && !above.sprite &&
          !above.fore && rows[y][x] === '#' && rnd() < 0.2 && th.flora.length) {
        const fl = th.flora[(rnd() * th.flora.length) | 0];
        decos.push({ kind: 'flora', frame: fl, x, y: y - 1 });
      }
    }
  }

  // water decoration: bottom-row gaps get a water line (visual only)
  const water = [];
  if (def.water) {
    for (let x = 0; x < MAPW; x++) {
      if (!cells[x][15].block && rows[15].slice(0, MAPW).replace(/\.+$/, '').length > x) {
        water.push(x);
      }
    }
  }

  // validation
  const counts = {};
  doors.forEach(d => counts[d.id] = (counts[d.id] || 0) + 1);
  for (const id in counts) if (counts[id] !== 2) { console.warn(`[${def.name}] door id ${id} appears ${counts[id]}x`); }
  if (!start) { console.error(`[${def.name}] no start!`); valid = false; }

  return { name: def.name, theme: def.theme, cells, doors, decos, ways, water, start, valid };
}
