//   |  section boundary (camera/movement wall; pass only by door)
//   g guard  s sentry  n knight  a archer  G ghost  d dragon  f fish
//   O boulder rain  x fire bed  j flame jet  b fireball
//   h/H horiz platform  v/V vert platform  _ falling platform
//   Z serpent boss   X wyrm boss
//   T torch  W window  N banner  (decor)
